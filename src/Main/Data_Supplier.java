    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
        // TODO add your handling code here:
        try {
         
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/dbapotik","root","");
            cn.createStatement().executeUpdate("update tb_supplier set nama='"
                    +txtnama.getText()
                    +"',alamat='"+txtalamat.getText()
                    +"',notlp='"+txtnotlp.getText()
                    +"'where id='"+txtid.getText()+"'");
            tampilkan();
             reset();
        } catch (SQLException ex) {
            Logger.getLogger(Data_Supplier.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_UpdateActionPerformed

